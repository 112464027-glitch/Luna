// ── 匯出供 chat.js 使用 ────────────────────────────────────────────────────────
// 將所有知識整理成 Gemini 可讀的純文字格式
function buildKnowledgeContext() {
  const filled = knowledge.filter(k => !k.content.includes("【在這裡填入內容】"));
  if (filled.length === 0) return ""; // 若尚未填入任何知識，不附加 context

  const body = filled
    .map(k => `【${k.title}】\n${k.content}`)
    .join("\n\n---\n\n");

  return `\n\n=== 婦科衛教知識庫 ===\n${body}\n=== 知識庫結束 ===`;
}

module.exports = { buildKnowledgeContext };
